const mongoose = require('mongoose');
var User = require('../data/user.js');
var Content = require('../data/Contents.js');
const Schema = mongoose.Schema;

var comment = new Schema({

comment:String,
ImageUrl:String,
//ref array for likes.
likes:[{liker:{type: mongoose.Schema.Types.ObjectId, ref: 'User'},time:{type:Date,default:Date.now()}}],
user:{type:Schema.Types.ObjectId,ref:'User'}, //ref to contain comment generated by whom.
content:{type:Schema.Types.ObjectId,ref:'Content'},
//reply:[{replyBy:{type: mongoose.Schema.Types.ObjectId, ref: 'Comment'},time:{type:Date,default:Date.now()}}]
},

{ timestamps: { createdAt: 'created_at', updatedAt:'updated_at' } }
);


var Comment = mongoose.model("Comment",comment);
module.exports=Comment;